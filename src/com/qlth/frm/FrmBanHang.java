    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed

        Integer tienKhachDua = Integer.parseInt(txtTienKhachDua.getText());
        Integer tienTraLaiKhach = Integer.parseInt(txtTienTraLaiKhach.getText());
        if (tienTraLaiKhach < 0) {
            JOptionPane.showMessageDialog(this, "chưa đủ tiền thanh toán");
            return;
        }
        for (int i = 0; i < ttHH.size(); i++) {
            KhoDAO khoDAO = new KhoDAO();
            Kho k = new Kho();
            k.setMaSach(ttHH.get(i).getMaSach());
            k.setSoLuong(ttHH.get(i).getSoLuongCon());

            khoDAO.update2(k);
        }

        Integer tongTien = Integer.parseInt(lblTongTien.getText());
        HoaDonDAO hoaDonDAO = new HoaDonDAO();
        HoaDon hoaDon = new HoaDon(new Date(), tongTien, tienKhachDua, tienTraLaiKhach);
        hoaDonDAO.insert(hoaDon);
        
        Integer maHoaDon=hoaDonDAO.getMaHoaDon();

        for (int i = 0; i < ttHH.size(); i++) {
            NhatKyBanSachDAO nhatKyBanSachDAO=new NhatKyBanSachDAO();
            NhatKyBanSach nhatKyBanSach=
                    new NhatKyBanSach(ttHH.get(i).getMaSach(),ttHH.get(i).getSoLuong(), new Date(),maHoaDon);
            nhatKyBanSachDAO.insert(nhatKyBanSach);
        }

        JOptionPane.showMessageDialog(this, "Thanh toán thành công");
        loadDuLieuLenLuoi();
        ttHH.removeAll(ttHH);
        tableModel = new DefaultTableModel();
        //tạo cột
        tableModel.addColumn("Mã sách");
        tableModel.addColumn("Tên sách");
        tableModel.addColumn("Giá mua");
        tableModel.addColumn("Số lượng");

        for (HangHoa data : ttHH) {
            Vector vector = new Vector();
            vector.add(data.getMaSach());
            vector.add(data.getTenSach());
            vector.add(data.getGiaMua());
            vector.add(data.getSoLuong());

            //tạo hàng
            tableModel.addRow(vector);
        }
        tblGioHang.setModel(tableModel);
        tinhTongTien();
        pnThanhToan.setVisible(false);
    }//GEN-LAST:event_btnThanhToanActionPerformed

