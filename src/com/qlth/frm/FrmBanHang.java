    private void tinhTienDu() {
        Integer tongTien = Integer.parseInt(lblTongTien.getText());
        if (txtTienKhachDua.getText().isEmpty()) {
            txtTienTraLaiKhach.setText("");
        } else if (txtTienKhachDua.getText().equals("00")) {
            txtTienKhachDua.setText("");
            txtTienTraLaiKhach.setText("");
        } else {
            Integer tienKhachDua = Integer.parseInt(txtTienKhachDua.getText());
            Integer tienDu = tienKhachDua - tongTien;
            txtTienTraLaiKhach.setText(tienDu.toString());
        }
    }
