    private void btnThemVaoGioHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemVaoGioHangActionPerformed
        pnThanhToan.setVisible(false);

        int row = tblDanhMucSach.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn sản phẩm");
            return;
        }

        String maSach = (String) tblDanhMucSach.getValueAt(row, 0);

        String tenSach = (String) tblDanhMucSach.getValueAt(row, 1);

        String tenTacGia = (String) tblDanhMucSach.getValueAt(row, 2);

        String tenNhaCungCap = (String) tblDanhMucSach.getValueAt(row, 3);

        Integer giaMua = Integer.parseInt(tblDanhMucSach.getValueAt(row, 4).toString());

        Integer soLuong = Integer.parseInt(tblDanhMucSach.getValueAt(row, 5).toString());

        Integer soLuongMua = null;
        Integer soLuongCon = null;

        if (txtSoLuongKhachMua.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa điền số hàng muốn mua");
            return;
        }
        try {
            soLuongMua = Integer.parseInt(txtSoLuongKhachMua.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Điền không hợp lệ");
            return;
        }
        if (soLuongMua <= 0) {
            JOptionPane.showMessageDialog(this, "Điền không hợp lệ");
            return;
        }

        if (soLuongMua <= Integer.parseInt(lblSoLuongHangCon.getText())) {
            boolean check = false;
            for (int i = 0; i < ttHH.size(); i++) {
                if (ttHH.get(i).getMaSach().contains(maSach)) {

                    soLuongCon = soLuong - (ttHH.get(i).getSoLuong() + soLuongMua);
                    ttHH.get(i).setSoLuongCon(soLuongCon);
                    ttHH.get(i).setSoLuong(ttHH.get(i).getSoLuong() + soLuongMua);
                    check = true;
                    tableModel = new DefaultTableModel();
                    //tạo cột
                    tableModel.addColumn("Mã Sách");
                    tableModel.addColumn("Tên sách");
//                    tableModel.addColumn("Tác giả");
//                    tableModel.addColumn("Nhà xuất bản");
                    tableModel.addColumn("Giá");
                    tableModel.addColumn("Số lượng");

                    for (HangHoa data : ttHH) {
                        Vector vector = new Vector();
                        vector.add(data.getMaSach());
                        vector.add(data.getTenSach());
//                        vector.add(data.getTacGia());
//                        vector.add(data.getNhaXuatBan());
                        vector.add(data.getGiaMua());
                        vector.add(data.getSoLuong());

                        //tạo hàng
                        tableModel.addRow(vector);
                    }
                    tblGioHang.setModel(tableModel);
                    tblGioHang.getColumnModel().getColumn(1).setPreferredWidth(200);
                }
            }
            if (!check) {
                soLuongCon = soLuong - soLuongMua;

                HangHoa hh = new HangHoa();
                hh.setMaSach(maSach);
                hh.setTenSach(tenSach);
                hh.setTacGia(tenTacGia);
                hh.setNhaXuatBan(tenNhaCungCap);
                hh.setGiaMua(giaMua);
                hh.setSoLuong(soLuongMua);
                hh.setSoLuongCon(soLuongCon);

                ttHH.add(hh);

                tableModel = new DefaultTableModel();
                //tạo cột
                tableModel.addColumn("Mã Sách");
                tableModel.addColumn("Tên sách");
//                tableModel.addColumn("Tác giả");
//                tableModel.addColumn("Nhà xuất bản");
                tableModel.addColumn("Giá");
                tableModel.addColumn("Số lượng");
                for (HangHoa data : ttHH) {
                    Vector vector = new Vector();
                    vector.add(data.getMaSach());
                    vector.add(data.getTenSach());
//                    vector.add(data.getTacGia());
//                    vector.add(data.getNhaXuatBan());
                    vector.add(data.getGiaMua());
                    vector.add(data.getSoLuong());

                    //tạo hàng
                    tableModel.addRow(vector);
                }
                tblGioHang.setModel(tableModel);
                tblGioHang.getColumnModel().getColumn(1).setPreferredWidth(200);
            }
            lblSoLuongHangCon.setText(soLuongCon.toString());
            tinhTongTien();
        } else {
            JOptionPane.showMessageDialog(this, "Không đủ hàng trong kho");
        }

    }//GEN-LAST:event_btnThemVaoGioHangActionPerformed

